let currentLang = 'ar';

function setLanguage(lang) {
    const isRTL = lang === "ar" || lang === "ur";
    document.body.dir = isRTL ? "rtl" : "ltr";
    document.documentElement.lang = lang;

    // Update body class for CSS styling
    document.body.className = ''; // Clear previous language classes
    document.body.classList.add(lang);
    document.body.classList.add(isRTL ? 'rtl' : 'ltr');

    document.querySelectorAll("[data-lang-key]").forEach(el => {
        const key = el.getAttribute("data-lang-key");
        if (translations[lang] && translations[lang][key]) {
            el.textContent = translations[lang][key];
        }
    });

    // إعادة ضبط اتجاه النصوص الداخلية
    document.querySelectorAll(".nested-list-subheader, .instruction-item").forEach(el => {
        el.style.textAlign = isRTL ? "right" : "left";
        el.style.direction = isRTL ? "rtl" : "ltr";
    });
}



function updateImageSources(lang) {
    const images = document.querySelectorAll('.lang-image');
    images.forEach(img => {
        const imageName = img.getAttribute('data-img-name');
        // The image folder name will match the language code (e.g., 'img/ar/', 'img/en/', 'img/fil/', 'img/ur/')
        const langFolder = lang;
        img.src = `img/${langFolder}/${imageName}`;
    });
}

document.addEventListener('DOMContentLoaded', function() {
    const isRTL = document.body.classList.contains('rtl') || document.body.dir === 'rtl';
    // Language switcher - cycles through ar -> en -> fil -> ur -> ne -> ar
    document.getElementById('lang-switcher').addEventListener('click', function() {
        let nextLang;
        if (currentLang === 'ar') {
            nextLang = 'en';
        } else if (currentLang === 'en') {
            nextLang = 'fil';
        } else if (currentLang === 'fil') {
            nextLang = 'ur';
        } else if (currentLang === 'ur') {
            nextLang = 'ne';
        } else if (currentLang === 'ne') {
            nextLang = 'ar';
        }
        currentLang = nextLang; // Update the current language
        setLanguage(nextLang);
    });
    setLanguage(currentLang);

    // Accordion for main sections
    document.querySelectorAll('.card-header').forEach(header => {
        header.addEventListener('click', function() {
            const sectionCard = this.closest('.section-card');
            const content = this.nextElementSibling;
            const isActive = sectionCard.classList.contains('active');

         

            // Close all sections
            document.querySelectorAll('.section-card').forEach(card => {
                card.classList.remove('active');
                card.querySelector('.card-content').classList.remove('expanded');
            });

            // If the clicked section was not already active, open it
            if (!isActive) {
                sectionCard.classList.add('active');
                content.classList.add('expanded');
            }
        });
    });

    // Accordion for nested links
    document.querySelectorAll('.nested-toggle').forEach(toggle => {
        toggle.addEventListener('click', function(e) {
            e.stopPropagation(); // Prevent closing the main accordion
            const nestedLinks = this.nextElementSibling;
            const isActive = this.classList.contains('active');

            // Close all other nested lists within the same card
            this.closest('.card-content').querySelectorAll('.nested-links').forEach(nl => {
                if (nl !== nestedLinks) {
                    nl.classList.remove('expanded');
                    nl.previousElementSibling.classList.remove('active');
                }
            });

            // Toggle the clicked nested list
            this.classList.toggle('active');
            nestedLinks.classList.toggle('expanded');
        });
    });

    // Awareness content switching (Closes accordion on selection)
    document.querySelectorAll('.awareness-link').forEach(link => {
        link.addEventListener('click', function(e) {
            e.preventDefault();
            const contentId = this.getAttribute('data-content-id');
            
            // Hide all awareness cards
            document.querySelectorAll('.awareness-content-card').forEach(card => {
                card.style.display = 'none';
                card.classList.remove('active');
            });

            // Show the target card
            const targetCard = document.getElementById(contentId);
            if (targetCard) {
                targetCard.style.display = 'block';
                targetCard.classList.add('active');
            }
            
            // Close the parent accordion
            const parentCard = this.closest('.section-card');
            if (parentCard) {
                parentCard.classList.remove('active');
                parentCard.querySelector('.card-content').classList.remove('expanded');
            }
        });
    });

    // Lightbox functionality (assuming it exists in the new HTML)
    const lightboxOverlay = document.getElementById('lightbox-overlay');
    if (lightboxOverlay) {
        document.querySelectorAll('.awareness-images img').forEach(img => {
            img.addEventListener('click', function() {
                document.getElementById('lightbox-img').src = this.src;
                lightboxOverlay.classList.add('active');
            });
        });

        document.querySelector('.lightbox-close').addEventListener('click', function() {
            lightboxOverlay.classList.remove('active');
        });

        lightboxOverlay.addEventListener('click', function(e) {
            if (e.target === this) {
                this.classList.remove('active');
            }
        });
    }



    // Adjust text alignment and direction for subheaders and instructions
    document.querySelectorAll('.nested-list-subheader, .instruction-item').forEach(el => {
        el.style.textAlign = isRTL ? 'right' : 'left';
        el.style.direction = isRTL ? 'rtl' : 'ltr';
    });


});